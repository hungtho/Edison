#pragma section
#pragma section ".boot" x
void Boot ( void )
{
	__asm("ja 0x80100020");
}
#pragma section

/*--------------------------------------------------------------------------*/
/* Function  |                                                              */
/* Name      |                                                              */
/*--------------------------------------------------------------------------*/
/* Parameter | Nothing                                                      */
/* Return    | Nothing                                                      */
/*--------------------------------------------------------------------------*/
int core0_main(void)
{
	( void )IfxCpu_enableInterrupts();

	IfxScuWdt_enableCpuWatchdog ( IfxScuWdt_getCpuWatchdogPassword () );
	IfxScuWdt_enableSafetyWatchdog ( IfxScuWdt_getSafetyWatchdogPassword () );
	IfxScuWdt_changeCpuWatchdogReload ( IfxScuWdt_getCpuWatchdogPassword (), 0xF8D9U ); /* WDT window: 100 ms*/
	IfxScuWdt_changeSafetyWatchdogReload ( IfxScuWdt_getSafetyWatchdogPassword (), 0xF8D9U ); /* WDT window: 100 ms*/
	IfxScuWdt_enableWatchdogWithDebugger ();

	/* Cpu sync event wait*/
	IfxCpu_emitEvent ( &g_cpuSyncEvent );
	if( IfxCpu_waitEvent ( &g_cpuSyncEvent, 1U ) == ( boolean)1U ){}

	RTE_BSW_Init ();

	RTE_OBC_ASW_Init ();
	RTE_LDC_ASW_Init ();

	for( ; ; )
	{
		while( u8g_task_flag_100u != 1U ){}
		u8g_task_flag_100u = 0U;

		RTE_Task_Run ();
	}

	return (1);
}


