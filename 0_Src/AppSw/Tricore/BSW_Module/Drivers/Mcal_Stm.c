void Mcal_StmInit ( void )
{
	initTime();

  IfxStm_initCompareConfig ( &stg_STMConf );           /* Initialize the configuration structure with default values   */

  stg_STMConf.triggerPriority = ISR_PRIORITY_STM;   /* Set the priority of the interrupt                            */
  stg_STMConf.typeOfService = IfxSrc_Tos_cpu0;      /* Set the service provider for the interrupts                  */
  stg_STMConf.ticks = TIMER_INT_TIME;               /* Set the number of ticks after which the timer triggers an
                                                   * interrupt for the first time                                 */
  IfxStm_initCompare ( STM_0, &stg_STMConf);            /* Initialize the STM with the user configuration               */
}

